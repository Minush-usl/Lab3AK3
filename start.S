.syntax unified
.cpu cortex-m4
//.fpu softvfp
.thumb

// Global memory locations.
.global vtable
.global hard_reset

/*
 * vector table 
 */
.type vtable, %object
.type hard_reset, %function
.section .interrupt_vector
vtable:
    .word stack_start 
    .word __hard_reset+1
    .size vtable, .-vtable
.section .text
hard_reset:
// initialize stack here
// if not initialized yet
    .data
    d: .asciz "starting \n"
    .text
    ldr r0, =d
    bl dbgput_line
    bl bootload
    _loop: b _loop
    .size hard_reset, .-hard_reset
